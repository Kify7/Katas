// Crea una función llamada farenheitToCelsius() que reciba 
// como parametro los grados farenheit 
// y los convierta a grados centígrados.

function farenheitToCelsius(farenheit) {
    var celsius = (farenheit - 32) * 5/9 ; 
    return celsius
}
